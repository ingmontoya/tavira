<script setup lang="ts">
import ValidationErrors from '@/components/ValidationErrors.vue';
import { Badge } from '@/components/ui/badge';
import { Button } from '@/components/ui/button';
import { Card, CardContent, CardHeader, CardTitle } from '@/components/ui/card';
import { Input } from '@/components/ui/input';
import { Label } from '@/components/ui/label';
import { Select, SelectContent, SelectItem, SelectTrigger, SelectValue } from '@/components/ui/select';
import { Textarea } from '@/components/ui/textarea';
import AppLayout from '@/layouts/AppLayout.vue';
import { Head, useForm, usePage } from '@inertiajs/vue3';
import { AlertTriangle, ArrowLeft, Building2, FileText, MapPin, Phone, Save, User } from 'lucide-vue-next';
import { computed } from 'vue';

// Breadcrumbs
const breadcrumbs = [
    {
        title: 'Escritorio',
        href: '/dashboard',
    },
    {
        title: 'Proveedores',
        href: '/providers',
    },
    {
        title: 'Editar Proveedor',
    },
];

interface Provider {
    id: number;
    name: string;
    document_type: string;
    document_number: string;
    email?: string;
    phone?: string;
    address?: string;
    city?: string;
    country?: string;
    contact_name?: string;
    contact_phone?: string;
    contact_email?: string;
    notes?: string;
    tax_regime?: string;
    is_active: boolean;
    status_badge: {
        text: string;
        class: string;
    };
    expenses_count: number;
    created_at: string;
}

const props = defineProps<{
    provider: Provider;
}>();

// Get page data for errors
const page = usePage();
const errors = computed(() => page.props.errors || {});

// Form setup
const form = useForm({
    name: props.provider.name,
    document_type: props.provider.document_type,
    document_number: props.provider.document_number,
    email: props.provider.email || '',
    phone: props.provider.phone || '',
    address: props.provider.address || '',
    city: props.provider.city || '',
    country: props.provider.country || 'Colombia',
    contact_name: props.provider.contact_name || '',
    contact_phone: props.provider.contact_phone || '',
    contact_email: props.provider.contact_email || '',
    notes: props.provider.notes || '',
    tax_regime: props.provider.tax_regime || 'ninguno',
    is_active: props.provider.is_active,
});

// Submit form
const submit = () => {
    // Convert 'ninguno' back to empty string for backend
    const formData = { ...form.data() };
    if (formData.tax_regime === 'ninguno') {
        formData.tax_regime = '';
    }

    form.transform((_) => formData).put(`/providers/${props.provider.id}`, {
        onSuccess: () => {
            // Will redirect to show page
        },
    });
};

const cancel = () => {
    if (confirm('¿Está seguro de que desea cancelar? Los cambios no guardados se perderán.')) {
        window.history.back();
    }
};

// Document types for Colombian context
const documentTypes = [
    { value: 'NIT', label: 'NIT - Número de Identificación Tributaria' },
    { value: 'CC', label: 'CC - Cédula de Ciudadanía' },
    { value: 'CE', label: 'CE - Cédula de Extranjería' },
    { value: 'PA', label: 'PA - Pasaporte' },
    { value: 'RUT', label: 'RUT - Registro Único Tributario' },
];

// Tax regimes for Colombian context
const taxRegimes = [
    { value: 'ninguno', label: 'Seleccionar régimen' },
    { value: 'Responsable de IVA', label: 'Responsable de IVA' },
    { value: 'No responsable de IVA', label: 'No responsable de IVA' },
    { value: 'Gran Contribuyente', label: 'Gran Contribuyente' },
    { value: 'Autorretenedor', label: 'Autorretenedor' },
    { value: 'Régimen Simplificado', label: 'Régimen Simplificado' },
    { value: 'No Residente', label: 'No Residente' },
];

// Major Colombian cities
const colombianCities = [
    'Bogotá',
    'Medellín',
    'Cali',
    'Barranquilla',
    'Cartagena',
    'Cúcuta',
    'Soledad',
    'Ibagué',
    'Bucaramanga',
    'Soacha',
    'Santa Marta',
    'Villavicencio',
    'Valledupar',
    'Pereira',
    'Montería',
    'Manizales',
    'Pasto',
    'Neiva',
    'Palmira',
    'Armenia',
];

// Warning for providers with expenses
const hasExpenses = computed(() => props.provider.expenses_count > 0);
</script>

<template>
    <Head :title="`Editar ${provider.name}`" />

    <AppLayout :title="`Editar ${provider.name}`" :breadcrumbs="breadcrumbs">
        <div class="flex h-full flex-1 flex-col gap-4 overflow-x-auto rounded-xl p-4">
            <ValidationErrors :errors="errors" />

            <!-- Warning for providers with expenses -->
            <div v-if="hasExpenses" class="mb-6 rounded-lg border border-amber-200 bg-amber-50 p-4">
                <div class="flex items-start gap-2">
                    <AlertTriangle class="mt-0.5 h-4 w-4 flex-shrink-0 text-amber-600" />
                    <div>
                        <p class="text-sm font-medium text-amber-800">Precaución al editar</p>
                        <p class="text-xs text-amber-700">
                            Este proveedor tiene {{ provider.expenses_count }} gasto{{ provider.expenses_count !== 1 ? 's' : '' }} registrado{{
                                provider.expenses_count !== 1 ? 's' : ''
                            }}. Los cambios en el documento o nombre pueden afectar los reportes históricos.
                        </p>
                    </div>
                </div>
            </div>

            <form @submit.prevent="submit">
                <div class="space-y-6">
                    <!-- Header -->
                    <div class="flex items-center justify-between">
                        <div class="space-y-1">
                            <div class="flex items-center gap-3">
                                <h2 class="text-2xl font-semibold tracking-tight">Editar {{ provider.name }}</h2>
                                <Badge :class="provider.status_badge.class">{{ provider.status_badge.text }}</Badge>
                            </div>
                            <p class="text-sm text-muted-foreground">Modifique la información del proveedor</p>
                        </div>
                        <div class="flex items-center space-x-2">
                            <Button type="button" variant="outline" @click="cancel">
                                <ArrowLeft class="mr-2 h-4 w-4" />
                                Cancelar
                            </Button>
                            <Button type="submit" :disabled="form.processing">
                                <Save class="mr-2 h-4 w-4" />
                                {{ form.processing ? 'Guardando...' : 'Guardar Cambios' }}
                            </Button>
                        </div>
                    </div>

                    <div class="grid grid-cols-1 gap-6 lg:grid-cols-3">
                        <!-- Main Information -->
                        <div class="space-y-6 lg:col-span-2">
                            <!-- Basic Information -->
                            <Card>
                                <CardHeader>
                                    <CardTitle class="flex items-center gap-2">
                                        <Building2 class="h-5 w-5" />
                                        Información Básica
                                    </CardTitle>
                                </CardHeader>
                                <CardContent class="space-y-4">
                                    <div class="space-y-2">
                                        <Label for="name" class="required">Nombre o Razón Social</Label>
                                        <Input id="name" v-model="form.name" placeholder="Nombre del proveedor" required />
                                    </div>

                                    <div class="grid grid-cols-1 gap-4 md:grid-cols-2">
                                        <div class="space-y-2">
                                            <Label for="document_type" class="required">Tipo de Documento</Label>
                                            <Select v-model="form.document_type" required>
                                                <SelectTrigger>
                                                    <SelectValue placeholder="Seleccionar tipo" />
                                                </SelectTrigger>
                                                <SelectContent>
                                                    <SelectItem v-for="docType in documentTypes" :key="docType.value" :value="docType.value">
                                                        {{ docType.label }}
                                                    </SelectItem>
                                                </SelectContent>
                                            </Select>
                                        </div>

                                        <div class="space-y-2">
                                            <Label for="document_number" class="required">Número de Documento</Label>
                                            <Input
                                                id="document_number"
                                                v-model="form.document_number"
                                                placeholder="Número sin puntos ni guiones"
                                                required
                                            />
                                            <p v-if="hasExpenses" class="text-xs text-amber-600">
                                                Cambiar el documento puede afectar reportes históricos
                                            </p>
                                        </div>
                                    </div>

                                    <div class="space-y-2">
                                        <Label for="tax_regime">Régimen Tributario</Label>
                                        <Select v-model="form.tax_regime">
                                            <SelectTrigger>
                                                <SelectValue placeholder="Seleccionar régimen" />
                                            </SelectTrigger>
                                            <SelectContent>
                                                <SelectItem v-for="regime in taxRegimes" :key="regime.value" :value="regime.value">
                                                    {{ regime.label }}
                                                </SelectItem>
                                            </SelectContent>
                                        </Select>
                                    </div>
                                </CardContent>
                            </Card>

                            <!-- Contact Information -->
                            <Card>
                                <CardHeader>
                                    <CardTitle class="flex items-center gap-2">
                                        <Phone class="h-5 w-5" />
                                        Información de Contacto
                                    </CardTitle>
                                </CardHeader>
                                <CardContent class="space-y-4">
                                    <div class="grid grid-cols-1 gap-4 md:grid-cols-2">
                                        <div class="space-y-2">
                                            <Label for="email">Email Principal</Label>
                                            <Input id="email" v-model="form.email" type="email" placeholder="email@proveedor.com" />
                                        </div>

                                        <div class="space-y-2">
                                            <Label for="phone">Teléfono Principal</Label>
                                            <Input id="phone" v-model="form.phone" placeholder="Número de teléfono" />
                                        </div>
                                    </div>

                                    <div class="space-y-2">
                                        <Label for="contact_name">Persona de Contacto</Label>
                                        <Input id="contact_name" v-model="form.contact_name" placeholder="Nombre del contacto principal" />
                                    </div>

                                    <div class="grid grid-cols-1 gap-4 md:grid-cols-2">
                                        <div class="space-y-2">
                                            <Label for="contact_email">Email de Contacto</Label>
                                            <Input
                                                id="contact_email"
                                                v-model="form.contact_email"
                                                type="email"
                                                placeholder="contacto@proveedor.com"
                                            />
                                        </div>

                                        <div class="space-y-2">
                                            <Label for="contact_phone">Teléfono de Contacto</Label>
                                            <Input id="contact_phone" v-model="form.contact_phone" placeholder="Número de contacto directo" />
                                        </div>
                                    </div>
                                </CardContent>
                            </Card>

                            <!-- Location Information -->
                            <Card>
                                <CardHeader>
                                    <CardTitle class="flex items-center gap-2">
                                        <MapPin class="h-5 w-5" />
                                        Información de Ubicación
                                    </CardTitle>
                                </CardHeader>
                                <CardContent class="space-y-4">
                                    <div class="space-y-2">
                                        <Label for="address">Dirección</Label>
                                        <Input id="address" v-model="form.address" placeholder="Dirección completa" />
                                    </div>

                                    <div class="grid grid-cols-1 gap-4 md:grid-cols-2">
                                        <div class="space-y-2">
                                            <Label for="city">Ciudad</Label>
                                            <Input id="city" v-model="form.city" placeholder="Ciudad" list="cities" />
                                            <datalist id="cities">
                                                <option v-for="city in colombianCities" :key="city" :value="city" />
                                            </datalist>
                                        </div>

                                        <div class="space-y-2">
                                            <Label for="country">País</Label>
                                            <Input id="country" v-model="form.country" placeholder="País" />
                                        </div>
                                    </div>
                                </CardContent>
                            </Card>

                            <!-- Additional Information -->
                            <Card>
                                <CardHeader>
                                    <CardTitle class="flex items-center gap-2">
                                        <FileText class="h-5 w-5" />
                                        Información Adicional
                                    </CardTitle>
                                </CardHeader>
                                <CardContent class="space-y-4">
                                    <div class="space-y-2">
                                        <Label for="notes">Notas y Observaciones</Label>
                                        <Textarea
                                            id="notes"
                                            v-model="form.notes"
                                            placeholder="Información adicional, términos especiales, etc."
                                            rows="4"
                                        />
                                    </div>
                                </CardContent>
                            </Card>
                        </div>

                        <!-- Sidebar -->
                        <div class="space-y-6">
                            <!-- Status Configuration -->
                            <Card>
                                <CardHeader>
                                    <CardTitle class="flex items-center gap-2">
                                        <User class="h-5 w-5" />
                                        Configuración de Estado
                                    </CardTitle>
                                </CardHeader>
                                <CardContent class="space-y-4">
                                    <div class="flex items-center space-x-2">
                                        <input
                                            type="checkbox"
                                            id="is_active"
                                            :checked="form.is_active"
                                            @change="
                                                (event) => {
                                                    form.is_active = (event.target as HTMLInputElement).checked;
                                                }
                                            "
                                            class="h-4 w-4 rounded border-gray-300 text-primary focus:ring-primary"
                                        />
                                        <Label for="is_active"> Proveedor activo </Label>
                                    </div>
                                    <p class="text-xs text-muted-foreground">
                                        Los proveedores inactivos no aparecerán en las listas de selección para nuevos gastos.
                                    </p>
                                    <p v-if="hasExpenses && !form.is_active" class="text-xs text-amber-600">
                                        Desactivar el proveedor no afecta los gastos existentes.
                                    </p>
                                </CardContent>
                            </Card>

                            <!-- Provider Information -->
                            <Card>
                                <CardHeader>
                                    <CardTitle class="text-sm">Información del Proveedor</CardTitle>
                                </CardHeader>
                                <CardContent class="space-y-3">
                                    <div class="flex items-center justify-between text-sm">
                                        <span class="text-muted-foreground">Gastos registrados:</span>
                                        <span class="font-medium">{{ provider.expenses_count }}</span>
                                    </div>
                                    <div class="flex items-center justify-between text-sm">
                                        <span class="text-muted-foreground">Estado actual:</span>
                                        <Badge :class="provider.status_badge.class">{{ provider.status_badge.text }}</Badge>
                                    </div>
                                    <div class="flex items-center justify-between text-sm">
                                        <span class="text-muted-foreground">Registrado:</span>
                                        <span>{{ new Date(provider.created_at).toLocaleDateString('es-CO') }}</span>
                                    </div>
                                </CardContent>
                            </Card>

                            <!-- Help Information -->
                            <Card>
                                <CardHeader>
                                    <CardTitle class="text-sm">Información de Ayuda</CardTitle>
                                </CardHeader>
                                <CardContent class="space-y-3 text-sm">
                                    <div>
                                        <h4 class="mb-1 font-medium">Campos Requeridos</h4>
                                        <p class="text-xs text-muted-foreground">
                                            Solo el nombre y documento son obligatorios. La información de contacto es opcional pero recomendada.
                                        </p>
                                    </div>
                                    <div v-if="hasExpenses">
                                        <h4 class="mb-1 font-medium">Proveedor con Historial</h4>
                                        <p class="text-xs text-muted-foreground">
                                            Este proveedor tiene gastos registrados. Los cambios en información básica pueden afectar reportes.
                                        </p>
                                    </div>
                                    <div>
                                        <h4 class="mb-1 font-medium">Estado Activo/Inactivo</h4>
                                        <p class="text-xs text-muted-foreground">
                                            Los proveedores inactivos no se pueden seleccionar en nuevos gastos, pero mantienen su historial.
                                        </p>
                                    </div>
                                </CardContent>
                            </Card>
                        </div>
                    </div>
                </div>
            </form>
        </div>
    </AppLayout>
</template>

<style scoped>
.required::after {
    content: ' *';
    color: red;
}
</style>
